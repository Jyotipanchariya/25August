#include<stdio.h>
int main()
{
    char a='a';
    for(char i=1; i<=4; i++)
    {
        for(char j=1;j<=i;j++)
        {
            printf("%c",a);
        }
        a++;
        printf("\n");
    }
    return 0;
}
